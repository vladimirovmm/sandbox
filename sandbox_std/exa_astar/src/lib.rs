//! Алгоритм A* (A-star) — это алгоритм поиска кратчайшего пути во взвешенном графе,
//! который использует эвристику для направления поиска и повышения эффективности.
//!
//! Принцип работы:
//! A* использует функцию f(x) = g(x) + h(x), где:
//! - g(x) — фактическая стоимость пути от начальной вершины до текущей вершины x;
//! - h(x) — эвристическая оценка стоимости пути от текущей вершины x до целевой вершины.
//!
//! Алгоритм использует очередь с приоритетом (открытый список), где вершины упорядочены
//! по возрастанию f(x). На каждом шаге извлекается вершина с наименьшим f(x),
//! исследуются её соседи, и для них пересчитываются значения g и f.
//!
//! Ключевые свойства:
//! - Оптимальность: если эвристика допустима, алгоритм гарантированно находит кратчайший путь.
//! - Полнота: если решение существует, алгоритм его найдёт.
//! - Оптимальная эффективность: при допустимой эвристике A* раскрывает минимальное количество вершин.
//!
//! Требования к эвристике:
//! - Допустимая: h(x) не должна переоценивать реальную стоимость достижения цели.
//! - Монотонная: для любой вершины v1 и её потомка v2 разность h(v1) - h(v2) не должна превышать
//!   фактического веса ребра от v1 до v2.
//!
//! Примеры допустимых эвристик:
//! - Манхэттенское расстояние: h(v) = |v.x - goal.x| + |v.y - goal.y|
//! - Расстояние Чебышева: h(v) = max(|v.x - goal.x|, |v.y - goal.y|)

